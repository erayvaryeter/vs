function setMarkdownContent(content) {
    let stylesFromHTMLElement = document.querySelector("html")?.style?.cssText;
    let stylesFromCssPlugin = document.querySelector("#pluginCss")?.innerHTML;
    let styles = `<style>html { ${stylesFromHTMLElement} }\n ${stylesFromCssPlugin}</style>`;
    // We need to allow the `target="_blank"` attribute so that hyperlinks open in an external browser
    let sanitizedContent = DOMPurify.sanitize(content, {ADD_ATTR: ['target']});
    document.querySelector('#markdownContent').srcdoc = styles + sanitizedContent;
}

function scrollToTop() {
    window.scrollTo(0, 0);
}

// This is where we add listen
